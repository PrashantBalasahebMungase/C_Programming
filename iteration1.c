/*
        Start

                Step 1 : Declare an integer variable i and initialize it to 0

                Step 2 : Use a for loop to repeat steps from i = 1 to i <= 10

                Step 3 : In each loop iteration, print "jay Ganesh"

                Step 4 : After the loop ends, return 0 to end the program

        End

  */         
#include<stdio.h>
int main()
{
    int i = 0;
    for(i=1; i <=10; i++)
    {
        printf("jay Ganesh\n");
    }
    return 0;
}